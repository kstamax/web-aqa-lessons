# --- for: перебір колекції ---
# Використовується, коли відома колекція для перебору.

products = [
    "Sauce Labs Backpack",
    "Sauce Labs Bike Light",
    "Sauce Labs Bolt T-Shirt",
]

for product in products:
    print(product)

# enumerate() — додає порядковий номер до кожного елемента
print("---")
for i, product in enumerate(products, 1):
    print(f"{i}. {product}")

# range(N) — генерує числа від 0 до N-1
print("---")
for i in range(3):
    print(f"Крок {i}")


# --- while: повторення поки виконується умова ---
